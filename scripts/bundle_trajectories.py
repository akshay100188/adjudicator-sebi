"""WI-4 — bundle the per-query TRAJ JSON into a single data.js the static viewer loads on file://.

The static trajectory viewer (frontend/trajectory-viewer/index.html) must open with no server, so it
cannot fetch() local JSON (browsers block file:// fetch). We inline the trajectories into a JS bundle
that sets window.TRAJECTORIES. The per-query JSON is written by scripts/run_agent_eval.py from real
agent runs; this step just concatenates them in a stable display order.

Usage:  python scripts/run_agent_eval.py   # produces trajectories/TRAJ-*.json from real runs
        python scripts/bundle_trajectories.py
"""
import json
from pathlib import Path

ROOT = Path(__file__).resolve().parent.parent
VIEWER = ROOT / "frontend" / "trajectory-viewer"
TRAJ_DIR = VIEWER / "trajectories"

# Display order: headline multi-hop supersession first, then the simple/correction contrasts.
ORDER = ["TRAJ-T01", "TRAJ-T02", "TRAJ-T03", "TRAJ-T04"]


def main() -> int:
    files = {p.stem: p for p in TRAJ_DIR.glob("TRAJ-*.json")}
    ordered = [n for n in ORDER if n in files] + sorted(set(files) - set(ORDER))
    data = [json.loads(files[n].read_text(encoding="utf-8")) for n in ordered]
    (VIEWER / "data.js").write_text(
        "// Auto-generated by scripts/bundle_trajectories.py — real agent runs (Phase 7 WI-4).\n"
        "// Regenerate: python scripts/run_agent_eval.py && python scripts/bundle_trajectories.py\n"
        "window.TRAJECTORIES = " + json.dumps(data, ensure_ascii=False, indent=2) + ";\n",
        encoding="utf-8",
    )
    print(f"data.js written — {len(data)} trajectories: {', '.join(ordered)}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
